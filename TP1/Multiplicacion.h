/** \brief multiplica dos numeros enteros
 *
 * \param a numero a multiplicar
 * \param b numero que multiplica
 * \return multiplicacion entre a y b
 *
 */


int Multiplicacion(int a, int b)
{
    int Resultado;

    Resultado = a * b;

    return Resultado;
}
